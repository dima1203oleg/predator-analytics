            if code.endswith("```"): code = code[:-3]
                
            safe_name = source_metadata.get('name', 'unknown').replace("-", "_").lower()
            file_path = f"/Users/Shared/Predator_60/services/ingestion-worker/app/pipelines/auto_generated/{safe_name}_pipeline.py"
            
            with open(file_path, "w", encoding="utf-8") as f:
                f.write(code)
            logger.info(f"Pipeline successfully generated: {file_path}")
            return file_path
